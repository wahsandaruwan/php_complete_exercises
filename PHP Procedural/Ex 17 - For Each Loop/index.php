<?php
    // For each loop
    // Basically use with colections
    // Don't need to increment a number
    // Dont't have to know the collection size
    // It will display all the elements of the collection

    $array = array("Japan","SriLanka","America","China","India");
    foreach($array as $i){
        echo "My country is ".$i."<br>";
    }

    // $i is use to point each element of the array
    // So therefore we have to echo $i
?>